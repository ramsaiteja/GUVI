import java.util.*;
import java.lang.*;
import java.io.*;
class Positive
{
	public static void main (String[] args) throws java.lang.Exception
	{
		Scanner sc=new Scanner(System.in);
		int s=sc.nextInt();
		if(s>0)
		{
			System.out.println("Positive");
		}
		else
		{
			System.out.println("Zero");
		}
	}
}
